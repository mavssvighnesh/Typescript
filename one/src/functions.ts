// Functions in TypeScript 

// Syntax: function functionName(parameters)
// Function to print the given input 
function print(x: any): void {
    console.log(x);
}

print("vignesh"); // prints "vignesh" as output 

// Short inline functions 
// These functions can be used with the keyword function  

const print2 = (y: any): void => { console.log(y); };

print2("vamsi"); // prints "vamsi" as output 
