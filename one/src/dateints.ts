//date in ts 
const currentDate: Date = new Date(); // using the Date object to get today's date 
console.log(currentDate); // printing the current date and time of the machine 

const customDate: Date = new Date();
customDate.setDate(23); // setting the date to 23 using the setDate method 
customDate.setMonth(12); // setting the month to January using the setMonth method 
customDate.setFullYear(2004); // setting the year to 2004 using the setFullYear method 
console.log(customDate); // printing the date 
